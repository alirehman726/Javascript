// Primitive Datatype in Javascript



// 1. String

let  name = "Rehmanali"
console.log("My String is:- " + name);
console.log("Datatype is:- " + (typeof name));


// 2. Integer

let  number = 50;
console.log("Datatype is:- " + (typeof number));


// 3. Boolen

let  valueBoolen = true;
console.log("Datatype is:- " + (typeof valueBoolen));


// 4. Null

let  valueNull = null;
console.log("Datatype is:- " + (typeof valueNull));


// 5. Null

let  valuedef = undefined;
console.log("Datatype is:- " + (typeof valuedef));



// Reference Datatype

// 1. Array

let  arr = [1, 2, 3, 4, true, 'Rehmanali', null]
console.log("Datatype is:- " + (typeof arr));



// 2. Object Literals

let  studentsMarks = {
    rehmanali: 50,
    imtiyaz: 75,
    naqvi: 85,
}   
console.log(studentsMarks);
console.log(typeof studentsMarks);


